// string concatenation
console.log("Hello" + "World"); // Output: HelloWorld

// adding number to a string
console.log("The answer is: " + 42); // Output: The answer is: 42

// adding booleans together
console.log(true + false); // Output: 1 (true is converted to 1, false is converted to 0)

console.log(1 < 2 < 3);
console.log(1 > 2 > 3);
